// EndBlock executes all ABCI EndBlock logic respective to the capability module. It
// returns no validator updates.
func (am AppModule) EndBlock(ctx sdk.Context, endBlock abci.RequestEndBlock) []abci.ValidatorUpdate {
	//log := util.BuildLog(util.GetStructFuncName(&am), util.LmChainKeeper)
	blockNum := endBlock.Height 
	
	if (blockNum-core.MinerStartHeight)%int64(core.SpaceMinerBonusBlockNum) == 0 && ctx.BlockHeight() > core.MinerStartHeight {
		validatorBonus := am.keeper.ValidatorBonus(ctx)
		
		minerBonus := am.keeper.SpaceMinerBonusNew(ctx)
		
		totalMiner := minerBonus.Add(validatorBonus)
		
		am.keeper.UpdateDeflationMinerInfor(ctx, totalMiner, ctx.BlockHeight())
		
		am.keeper.SetSpaceTotalIndex(ctx)
	}
	
	am.keeper.ComplainResult(ctx)
	
	am.keeper.CalculateCopyrightVoteResult(ctx)
	
	am.keeper.CopyrightVoteRedeem(ctx)

	return []abci.ValidatorUpdate{}
}

